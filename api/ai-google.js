function extrairDestino(conteudo) {
    try {
        const texto = conteudo.toLowerCase();
        
        for (const [key, cidade] of Object.entries(DESTINOS_CONHECIDOS)) {
            if (texto.includes(key)) {
                console.log(`[${getTimestamp()}] ‚úÖ v2.82: Destino: ${cidade}`);
                return cidade;
            }
        }
        
        const codigosAeroporto = conteudo.match(/\b([A-Z]{3})\b/g);
        if (codigosAeroporto) {
            for (const codigo of codigosAeroporto) {
                if (AEROPORTOS[codigo] && !['GRU', 'CGH', 'SDU', 'GIG', 'VCP'].includes(codigo)) {
                    const cidade = AEROPORTOS[codigo];
                    console.log(`[${getTimestamp()}] ‚úÖ v2.82: Destino por c√≥digo ${codigo}: ${cidade}`);
                    return cidade;
                }
            }
        }
        
        return 'Lisboa';
        
    } catch (error) {
        console.error(`[${getTimestamp()}] ‚ùå v2.82: Erro extrair destino:`, error);
        return 'Lisboa';
    }
}

